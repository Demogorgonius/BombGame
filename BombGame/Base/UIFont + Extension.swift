import UIKit

extension UIFont {
    class var regular24: UIFont {
        return UIFont.systemFont(ofSize: 24, weight: .regular)
    }
    
    class var bold32: UIFont {
        return UIFont.systemFont(ofSize: 32, weight: .bold)
    }
    
    class var bold48: UIFont {
        return UIFont.systemFont(ofSize: 48, weight: .bold)
    }
}
